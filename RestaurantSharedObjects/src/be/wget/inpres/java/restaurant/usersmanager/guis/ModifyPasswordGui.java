    private void performChange() {
        try {
            StringBuilder oldPassword = new StringBuilder();
            for (int i = 0; i < this.oldPasswordTextfield.getPassword().length; i++) {
                oldPassword.append(this.oldPasswordTextfield.getPassword()[i]);
            }
            StringBuilder newPassword = new StringBuilder();
            for (int i = 0; i < this.newPasswordTextfield.getPassword().length; i++) {
                newPassword.append(this.newPasswordTextfield.getPassword()[i]);
            }
            this.usersManager.changeUserPassword(
                    this.userName,
                    oldPassword.toString(),
                    newPassword.toString());
        } catch (UsersManagerUserNotFoundException ex) {
            JOptionPane.showMessageDialog(this,
                "The user you specified is invalid.",
                "Username invalid",
                JOptionPane.ERROR_MESSAGE);
            return;
        } catch (UsersManagerPasswordInvalidException ex) {
            JOptionPane.showMessageDialog(this,
                "The current password of the current user is invalid.",
                "Password invalid",
                JOptionPane.ERROR_MESSAGE);
            return;
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,
                "An issue occurred when trying to store the new password. " +
                    "The password might likely have not been changed.",
                "Serialization issue",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        this.setVisible(false);
    }

